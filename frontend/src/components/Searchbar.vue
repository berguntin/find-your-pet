<template>
    <div class="max-w-2xl mx-auto p-4">
      <div class="relative">
        <input
          type="text"
          v-model="searchQuery"
          placeholder="Buscar..."
          class="w-full pl-10 pr-4 py-2 rounded-full border border-gray-300 focus:outline-none focus:ring-2 focus:ring-blue-500 focus:border-transparent transition duration-300 ease-in-out bg-white text-gray-900 placeholder-gray-500"
          @input="handleSearch"
        />
        <div class="absolute left-3 top-1/2 transform -translate-y-1/2 text-gray-400">
          <SearchIcon class="w-5 h-5" />
        </div>
        <button
          v-if="searchQuery"
          @click="clearSearch"
          class="absolute right-3 top-1/2 transform -translate-y-1/2 text-gray-400 hover:text-gray-600 focus:outline-none"
        >
          <XIcon class="w-5 h-5" />
        </button>
      </div>
    </div>
  </template>
  
  <script setup>
  import { ref } from 'vue'
  import { SearchIcon, XIcon } from 'lucide-vue-next'
  import { defineEmits } from 'vue';

  const searchQuery = ref('')
  const emits = defineEmits(['updateQuery'])
  const timeout = ref(null)

  const handleSearch = () => {
        clearTimeout(timeout.value)
        timeout.value = setTimeout(() => {
            emits('updateQuery', searchQuery.value)
        }, 500)
  }
  
  const clearSearch = () => {
    searchQuery.value = ''
    emits('updateQuery', '')
  }
  </script>
  